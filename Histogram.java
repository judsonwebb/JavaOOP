import java.util.*;
import java.io.*;
public class Histogram
{
    public static void Result (double Amount)
    {
        for (int x = 0;x<=(Amount/1000);x++)
        {
            System.out.print("*");
        }
    }
    public static int randomnum(int Min, int Max)
    {
        Random Rand = new Random();
        int Number = Rand.nextInt((Max-Min)+1)+Min;
        return Number;
    }
    public static void main (String args[])
    {
        System.out.println("Day\tDaily\tSales Graph");
        
        for(int x= 1; x<31;x++)
        {
            int RandomNum=0;
            if ((x%7)==1)
            {
                RandomNum = randomnum(30000,50000);
            }
            else if ((x%7)==2)
            {
                RandomNum = randomnum(20000,50000);
            }
            else if ((x%7)==6)
            {
                RandomNum = randomnum(10000,15000);
            }
            else
            {
                 RandomNum = randomnum(10000,50000);
            }
            
            if((x%7)!=0)
            {
                System.out.print((x+1)+"\t");
                System.out.print(RandomNum + "\t");
                Result(RandomNum);
                System.out.println();
            }
            if((x%7)==0)
            {
                System.out.println();
            }
        }
    }
    
}
